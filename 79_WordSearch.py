class Node:
    def __init__(self, h, v):
        self.r = h
        self.c = v


def find(board, word):
    def se(tw, node):
        if node.r >= len(board) or node.r < 0 or node.c >= len(board[0]) or node.c < 0:
            return False
        elif (len(tw) == 1 and board[node.r][node.c] == tw[0]) or len(tw) == 0:
            return True
        elif board[node.r][node.c] == tw[0]:
            temp = board[node.r][node.c]
            board[node.r][node.c] = "#"
            t1 = se(tw[1:], Node(node.r - 1, node.c))
            if t1:
                return True
            t2 = se(tw[1:], Node(node.r, node.c - 1))
            if t2:
                return True
            t3 = se(tw[1:], Node(node.r + 1, node.c))
            if t3:
                return True
            t4 = se(tw[1:], Node(node.r, node.c + 1))
            if t4:
                return True
            board[node.r][node.c] = temp
            return False
        else:
            return False

    for i in range(len(board)):
        for j in range(len(board[i])):
            if board[i][j] == word[0] and se(word, Node(i, j)):
                return True
    return False


